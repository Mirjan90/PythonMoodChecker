mood = input("In what mood are you today, please choose between HAPPY, NERVOUS, SAD, RELAXED: ")

if mood == "happy":
    print("It is great to see you happy!")

elif mood == "nervous":
    print("Take a deep breath 3 times.")

elif mood == "sad":
    print("Dont be sad, life is too short")

elif mood == "relaxed":
    print("Just enjoy the day!")

else:
    print("I do not recognize the mood.")